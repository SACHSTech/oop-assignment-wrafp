package FoodDelivery;
import FoodDelivery.*;

public class Item {
    
    // Instance variables
    private String name;
    private double price;
    private boolean isBought;

    // Methods
    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    public boolean setIsBought() {
        isBought = true;
        return isBought;
    }
}
